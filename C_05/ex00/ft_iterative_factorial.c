int	ft_iterative_factorial(int nb)
{
	int	c;

	c = 1;
	if (nb >= 0)
	{
		if (nb == 0)
			return (1);
		while (nb > 0)
		{
			c *= nb;
			nb--;
		}
		return (c);
	}
	return (0);
}
